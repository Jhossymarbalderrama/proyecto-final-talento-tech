
export class Product{       
    constructor(title, description, category, price, img, material, size, color, gender, stockAvailability, discount, releaseDate){
        this.title = title;
        this.description = description;
        this.category = category;
        this.price = price;
        this.img = img;
        this.material = material;
        this.size = size;
        this.color = color;
        this.gender = gender;
        this.stockAvailability = stockAvailability;
        this.discount = discount;
        this.releaseDate = releaseDate;
    }    
}